#include "pwm_drv.h"
#include "nrfx_pwm.h"

/******************************************************************************
 * LOCAL DEFINITION
 *****************************************************************************/
#define PORT_0 (0x00)
#define PORT_1 (0x20)

#define PIN_0  (0x00)
#define PIN_1  (0x01)
#define PIN_2  (0x02)
#define PIN_3  (0x03)
#define PIN_4  (0x04)
#define PIN_5  (0x05)
#define PIN_6  (0x06)
#define PIN_7  (0x07)
#define PIN_8  (0x08)
#define PIN_9  (0x09)
#define PIN_10 (0x0A)
#define PIN_11 (0x0B)
#define PIN_12 (0x0C)
#define PIN_13 (0x0D)
#define PIN_14 (0x0E)
#define PIN_15 (0x0F)

#define UNUSED_PIN NRFX_PWM_PIN_NOT_USED
#define LRA_CTRL_PIN (PIN_15+PORT_1)

