/**
 * @brief:
 * @param:
 * @return:
 */
void ActCtrlInit()
{
	uint8_t pwmPins[4] = {LRA_CTRL_PIN, UNUSED_PIN, UNUSED_PIN, UNUSED_PIN};
	PwmDrvInit(pwmPins);
	PwmDrvSetDutyCycle(0);
}

